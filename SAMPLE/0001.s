START:
		LD		DE,DATA01
ST1:	LD		A,(DE)
		CP		0FFH
		JR		Z,START
		CALL	DISP
		JR		ST1
		

DISP:
		LD		B,8
		LD		HL,SEGBUF+7
LP1:	LD		A,(DE)
		LD		(HL),A
		DEC		HL
		INC		DE
		DJNZ	LP1
		CALL	WAIT
		RET

WAIT:
		PUSH	HL
		PUSH	DE
		PUSH	BC
		PUSH	AF
		LD		B,10H
W1:
		CALL	SCAN
		DJNZ	W1
		POP		AF
		POP		BC
		POP		DE
		POP		HL
		RET
		
